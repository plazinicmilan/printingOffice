package com.src.printing.office.po.model.jasper;

public class JasperMaterialItem {

	private String rb;
	private String naziv;
	private String jedinicaMere;
	private Double kolicina;
	private String proizvodjac;
	private String tip;
	private Double pakovanjeKolicina;
	private String pakovanjeJedinicaMere;

	public String getRb() {
		return rb;
	}

	public void setRb(String rb) {
		this.rb = rb;
	}

	public String getNaziv() {
		return naziv;
	}

	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}

	public String getJedinicaMere() {
		return jedinicaMere;
	}

	public void setJedinicaMere(String jedinicaMere) {
		this.jedinicaMere = jedinicaMere;
	}

	public Double getKolicina() {
		return kolicina;
	}

	public void setKolicina(Double kolicina) {
		this.kolicina = kolicina;
	}

	public String getProizvodjac() {
		return proizvodjac;
	}

	public void setProizvodjac(String proizvodjac) {
		this.proizvodjac = proizvodjac;
	}

	public String getTip() {
		return tip;
	}

	public void setTip(String tip) {
		this.tip = tip;
	}

	public Double getPakovanjeKolicina() {
		return pakovanjeKolicina;
	}

	public void setPakovanjeKolicina(Double pakovanjeKolicina) {
		this.pakovanjeKolicina = pakovanjeKolicina;
	}

	public String getPakovanjeJedinicaMere() {
		return pakovanjeJedinicaMere;
	}

	public void setPakovanjeJedinicaMere(String pakovanjeJedinicaMere) {
		this.pakovanjeJedinicaMere = pakovanjeJedinicaMere;
	}

}
