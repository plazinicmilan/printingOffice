package com.src.printing.office.po.model.jasper;

public class JasperWorkOrderItem {

	private String rb;
	private String naziv;
	private String tabaka;
	private String visak;
	private String materijal;
	private String formatTabaka;
	private String stampa;
	private String napomena;

	public String getRb() {
		return rb;
	}

	public void setRb(String rb) {
		this.rb = rb;
	}

	public String getNaziv() {
		return naziv;
	}

	public void setNaziv(String naziv) {
		this.naziv = naziv;
	}

	public String getTabaka() {
		return tabaka;
	}

	public void setTabaka(String tabaka) {
		this.tabaka = tabaka;
	}

	public String getVisak() {
		return visak;
	}

	public void setVisak(String visak) {
		this.visak = visak;
	}

	public String getMaterijal() {
		return materijal;
	}

	public void setMaterijal(String materijal) {
		this.materijal = materijal;
	}

	public String getFormatTabaka() {
		return formatTabaka;
	}

	public void setFormatTabaka(String formatTabaka) {
		this.formatTabaka = formatTabaka;
	}

	public String getStampa() {
		return stampa;
	}

	public void setStampa(String stampa) {
		this.stampa = stampa;
	}

	public String getNapomena() {
		return napomena;
	}

	public void setNapomena(String napomena) {
		this.napomena = napomena;
	}

}
